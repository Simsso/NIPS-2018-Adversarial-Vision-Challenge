    filenames_labels = load_filenames_labels(mode)
    random.shuffle(filenames_labels)
    filenames = np.array([filename for filename, _ in filenames_labels])
    labels = np.array([label for _, label in filenames_labels])
    texts = labels 
    labels = labels.astype(np.uint8)

    _process_image_files(name=mode, filenames=filenames, texts=texts, labels=labels, num_shards=2)
    

